package DesignPatterns.Creational;

public class Car {
    private String color;
    private int qty_doors;

    public void setColor(String color) {
        this.color = color;
    }

    public void setQty_doors(int qty_doors) {
        this.qty_doors = qty_doors;
    }

    public String getColor() {
        return color;
    }

    public int getQty_doors() {
        return qty_doors;
    }
}
